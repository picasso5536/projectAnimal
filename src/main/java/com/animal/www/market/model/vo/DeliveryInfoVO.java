package com.animal.www.market.model.vo;

public class DeliveryInfoVO {
	private String dinfo_idx, dinfo_name, dinfo_addr, mbr_nickname, dinfo_msg;

	public String getDinfo_idx() {
		return dinfo_idx;
	}

	public void setDinfo_idx(String dinfo_idx) {
		this.dinfo_idx = dinfo_idx;
	}

	public String getDinfo_name() {
		return dinfo_name;
	}

	public void setDinfo_name(String dinfo_name) {
		this.dinfo_name = dinfo_name;
	}

	public String getDinfo_addr() {
		return dinfo_addr;
	}

	public void setDinfo_addr(String dinfo_addr) {
		this.dinfo_addr = dinfo_addr;
	}

	public String getMbr_nickname() {
		return mbr_nickname;
	}

	public void setMbr_nickname(String mbr_nickname) {
		this.mbr_nickname = mbr_nickname;
	}

	public String getDinfo_msg() {
		return dinfo_msg;
	}

	public void setDinfo_msg(String dinfo_msg) {
		this.dinfo_msg = dinfo_msg;
	}
}
